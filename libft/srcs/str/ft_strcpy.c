#include "libft.h"

string	ft_strcpy(string dest, cstring str)
{
	size_t	i;

	i = 0;
	while (str[i] != '\0')
	{
		dest[i] = str[i];
		++i;
	}
	dest[i] = '\0';
	return (dest);
}
